// DataType의 종류
// 1. primitive(int, float, char, boolean ... )
// 2. Reference(Class, Array ... )
let intV = 10;
let floatV = 10.5;
let stringV = "10";
let booleanV = true;

console.log(`intV = ${intV}`);
console.log(`floatV = ${floatV}`);
console.log(`stringV = ${stringV}`);
console.log(`booleanV = ${booleanV}`);